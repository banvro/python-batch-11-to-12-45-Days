    # Abstract Methods
    
    
    from abc import ABC, abstractmethod
    
    class vechical(ABC):
        @abstractmethod
        def tyers(self):
            pass
        
        @abstractmethod
        def body(self):
            pass
    
        @abstractmethod
        def engine(self):
            pass
    
    class oddi(vechical):
        def tyers(self):
            print("these are tyers")
        
        def body(self):
            print("this is body")
        
        def engine(self):
            print("this is an engine...")
            
    class tesla(vechical):
        pass
        
    
    obj1 = tesla()
    obj = oddi()
    
    
    
    
    
    
    
